    private void jButtonBackActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonBackActionPerformed
        int fieldLength = jTextFieldOuput.getText().length();
        int number = jTextFieldOuput.getText().length() - 1;
        String storeState;

        if (fieldLength > 0) {
            StringBuilder backspace = new StringBuilder(jTextFieldOuput.getText());
            backspace.deleteCharAt(number);
            storeState = backspace.toString();
            jTextFieldOuput.setText(storeState);

        }


    }//GEN-LAST:event_jButtonBackActionPerformed

