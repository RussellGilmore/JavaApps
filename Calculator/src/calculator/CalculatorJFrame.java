    public void arithmetic() {
        switch (this.calculation) {
            case 1:
                this.answer = this.number + Double.parseDouble(jTextFieldOuput.getText());
                jTextFieldOuput.setText(Double.toString(this.answer));
                break;
            case 2:
                this.answer = this.number - Double.parseDouble(jTextFieldOuput.getText());
                jTextFieldOuput.setText(Double.toString(this.answer));
                break;
            case 3:
                this.answer = this.number * Double.parseDouble(jTextFieldOuput.getText());
                jTextFieldOuput.setText(Double.toString(this.answer));
                break;

            case 4:
                this.answer = this.number / Double.parseDouble(jTextFieldOuput.getText());
                jTextFieldOuput.setText(Double.toString(this.answer));
                break;
        }
    }

